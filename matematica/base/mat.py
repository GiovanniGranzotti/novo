def soma(parc1, parc2): #função soma
    """Esta função calcula a soma de duas parcelas

    :param parc1: number
    :param parc2: number
    :return: number
    """
    return parc1 + parc2

if __name__ == '__main__':
    # Testando a função soma
    print(soma(2, 3))   # imprimindo
